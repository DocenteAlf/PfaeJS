let a = 5;
let b = 7;

document.write(a + b, " suma"+"<br>"); //suma
document.write(a - b, " resta"+"<br>"); //resta
document.write(a * b, " producto"+"<br>"); //multiplicacion
document.write(a / b, " división"+"<br>"); //división
document.write(a % b, " módulo"+"<br>"); //modulo
document.write(a ** b, " potencia"+"<br>"); //potencia

//incrementar
document.write("incrementar"+"<br>");
document.write(a, " a"+"<br>");
document.write(++a, " ++a"+"<br>");
document.write(a, " a"+"<br>");
document.write(a++, "a++"+"<br>");
document.write(a, " a"+"<br>");

//decrementar
document.write("decrementar"+"<br>");
document.write(b, " b"+"<br>");
document.write(--b, " --b"+"<br>");
document.write(b, " b"+"<br>");
document.write(b--," b--"+"<br>");
document.write(b, " b"+"<br>");